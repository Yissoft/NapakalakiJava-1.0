    public void setBC(BadConsequence bc) {
        bcModel = bc;
        
        //BadConsequenceText.setText(bcModel.getText());
        //BadConsequenceText.setEditable(false);
        labelNiveles.setText(Integer.toString(bcModel.getLevels()));
        LabelMuerte.setText(Boolean.toString(bcModel.getDeath()));
        
        if(bcModel.getSpecificVisibleTreasures() != null &&
           bcModel.getSpecificHiddenTreasures() != null){
            visibleTreasures.setText(bcModel.getSpecificVisibleTreasures().toString());
            hiddenTreasures.setText(bcModel.getSpecificHiddenTreasures().toString());
        }
        
        nVisibleTreasures.setText(Integer.toString(bcModel.getNVisibleTreasures()));
        nHiddenTreasures.setText(Integer.toString(bcModel.getNHiddenTreasures()));
        
        repaint();
    }

